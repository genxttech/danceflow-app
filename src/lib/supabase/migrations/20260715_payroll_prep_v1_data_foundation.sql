-- 4. Extend instructor earnings with payroll snapshots and batch linkage
-- -----------------------------------------------------------------------------

alter table public.instructor_earnings
  add column if not exists pay_period_id uuid
    references public.payroll_pay_periods(id) on delete restrict,
  add column if not exists payroll_batch_id uuid
    references public.payroll_batches(id) on delete restrict,
  add column if not exists worker_classification_snapshot text,
  add column if not exists accounting_category_snapshot text,
  add column if not exists taxable_compensation_amount numeric not null default 0,
  add column if not exists reimbursement_amount numeric not null default 0,
  add column if not exists deduction_amount numeric not null default 0,
  add column if not exists locked_at timestamptz;

do $$
begin
  if not exists (
    select 1
    from pg_constraint
    where conrelid = 'public.instructor_earnings'::regclass
      and conname = 'instructor_earnings_worker_classification_snapshot_check'
  ) then
    alter table public.instructor_earnings
      add constraint instructor_earnings_worker_classification_snapshot_check
      check (
        worker_classification_snapshot is null
        or worker_classification_snapshot in ('not_set', 'contractor', 'employee', 'owner')
      );
  end if;
end
$$;

create index if not exists idx_instructor_earnings_pay_period
  on public.instructor_earnings (studio_id, pay_period_id, status);

create index if not exists idx_instructor_earnings_payroll_batch
  on public.instructor_earnings (studio_id, payroll_batch_id, status);

-- Existing rows are absent in the audited dev database, but this keeps the
-- migration safe if another environment already contains compensation data.
update public.instructor_earnings ie
set
  worker_classification_snapshot = coalesce(
    ie.worker_classification_snapshot,
    ipp.worker_classification,
    'not_set'
  ),
  accounting_category_snapshot = coalesce(
    ie.accounting_category_snapshot,
    case
      when coalesce(ipp.worker_classification, 'not_set') = 'employee'
        then 'employee_wage_expense'
      else 'contract_labor_expense'
    end
  ),
  taxable_compensation_amount = case
    when ie.adjustment_type = 'reimbursement' then 0
    when ie.adjustment_type = 'deduction' then 0
    else greatest(coalesce(ie.earning_amount, 0), 0)
  end,
  reimbursement_amount = case
    when ie.adjustment_type = 'reimbursement'
      then greatest(coalesce(ie.earning_amount, 0), 0)
    else 0
  end,
  deduction_amount = case
    when ie.adjustment_type = 'deduction'
      then abs(coalesce(ie.earning_amount, 0))
    else 0
  end
from public.instructor_payroll_profiles ipp
where ipp.studio_id = ie.studio_id
  and ipp.instructor_id = ie.instructor_id
  and (
    ie.worker_classification_snapshot is null
    or ie.accounting_category_snapshot is null
    or (
      ie.taxable_compensation_amount = 0
      and ie.reimbursement_amount = 0
      and ie.deduction_amount = 0
    )
  );

