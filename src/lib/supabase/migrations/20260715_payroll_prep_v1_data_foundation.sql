-- =============================================================================
-- DanceFlow Payroll Prep V1 — Data Foundation
-- =============================================================================
-- Scope:
--   • Keep sensitive payroll attributes out of the general instructors table.
--   • Add payroll profiles, pay periods, payroll batches, earning snapshots,
--     and append-only compensation history.
--   • Limit payroll preparation to studio owners/admins.
--   • Reserve final paid actions for studio owners.
--   • Remove legacy hard-delete access.
--   • Do not change instructor earning calculation logic in this migration.
-- =============================================================================

-- -----------------------------------------------------------------------------
-- 1. Sensitive instructor payroll profiles
-- -----------------------------------------------------------------------------

create table if not exists public.instructor_payroll_profiles (
  id uuid primary key default gen_random_uuid(),
  studio_id uuid not null references public.studios(id) on delete cascade,
  instructor_id uuid not null references public.instructors(id) on delete cascade,

  worker_classification text not null default 'not_set'
    check (worker_classification in ('not_set', 'contractor', 'employee', 'owner')),

  payroll_active boolean not null default true,
  external_payroll_id text,
  payroll_notes text,

  created_by uuid references public.profiles(id) on delete set null,
  updated_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  unique (studio_id, instructor_id)
);

create index if not exists idx_instructor_payroll_profiles_studio
  on public.instructor_payroll_profiles (studio_id, instructor_id);

create unique index if not exists idx_instructor_payroll_profiles_external_id
  on public.instructor_payroll_profiles (studio_id, external_payroll_id)
  where external_payroll_id is not null;

-- Seed a non-sensitive default profile for every existing instructor.
insert into public.instructor_payroll_profiles (
  studio_id,
  instructor_id,
  worker_classification,
  payroll_active
)
select
  i.studio_id,
  i.id,
  'not_set',
  true
from public.instructors i
where not exists (
  select 1
  from public.instructor_payroll_profiles ipp
  where ipp.studio_id = i.studio_id
    and ipp.instructor_id = i.id
);

-- -----------------------------------------------------------------------------
-- 2. Pay periods
-- -----------------------------------------------------------------------------

create table if not exists public.payroll_pay_periods (
  id uuid primary key default gen_random_uuid(),
  studio_id uuid not null references public.studios(id) on delete cascade,

  period_start date not null,
  period_end date not null,
  pay_date date,
  status text not null default 'open'
    check (status in ('open', 'in_review', 'approved', 'paid', 'void')),

  compensation_total numeric not null default 0,
  reimbursement_total numeric not null default 0,
  deduction_total numeric not null default 0,
  net_payment_total numeric not null default 0,

  approved_at timestamptz,
  approved_by uuid references public.profiles(id) on delete set null,
  paid_at timestamptz,
  paid_by uuid references public.profiles(id) on delete set null,
  voided_at timestamptz,
  voided_by uuid references public.profiles(id) on delete set null,
  void_reason text,
  locked_at timestamptz,

  created_by uuid references public.profiles(id) on delete set null,
  updated_by uuid references public.profiles(id) on delete set null,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  check (period_end >= period_start),
  unique (studio_id, period_start, period_end)
);

create index if not exists idx_payroll_pay_periods_studio_dates
  on public.payroll_pay_periods (studio_id, period_start desc, period_end desc);

create index if not exists idx_payroll_pay_periods_studio_status
  on public.payroll_pay_periods (studio_id, status);

