-- -----------------------------------------------------------------------------
-- 7. Automatic append-only activity logging
-- -----------------------------------------------------------------------------

create or replace function public.log_compensation_activity()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  v_activity_type text;
  v_studio_id uuid;
  v_instructor_id uuid;
  v_earning_id uuid;
  v_pay_period_id uuid;
  v_payroll_batch_id uuid;
  v_prior_status text;
  v_new_status text;
  v_amount numeric;
begin
  if tg_table_name = 'instructor_earnings' then
    v_studio_id := new.studio_id;
    v_instructor_id := new.instructor_id;
    v_earning_id := new.id;
    v_pay_period_id := new.pay_period_id;
    v_payroll_batch_id := new.payroll_batch_id;
    v_prior_status := case when tg_op = 'UPDATE' then old.status else null end;
    v_new_status := new.status;
    v_amount := new.earning_amount;

    if tg_op = 'INSERT' then
      v_activity_type := 'earning_created';
    elsif old.status is distinct from new.status then
      v_activity_type := case new.status
        when 'approved' then 'earning_approved'
        when 'paid' then 'earning_paid'
        when 'void' then 'earning_voided'
        else 'earning_updated'
      end;
    elsif old.payroll_batch_id is distinct from new.payroll_batch_id
      and new.payroll_batch_id is not null then
      v_activity_type := 'earning_assigned_to_batch';
    elsif old.pay_period_id is distinct from new.pay_period_id
      and new.pay_period_id is not null then
      v_activity_type := 'earning_assigned_to_period';
    elsif old.pay_mode is distinct from new.pay_mode
      or old.earning_amount is distinct from new.earning_amount then
      v_activity_type := case
        when new.pay_mode = 'manual_override' then 'earning_overridden'
        else 'earning_updated'
      end;
    else
      v_activity_type := 'earning_updated';
    end if;

  elsif tg_table_name = 'payroll_pay_periods' then
    v_studio_id := new.studio_id;
    v_pay_period_id := new.id;
    v_prior_status := case when tg_op = 'UPDATE' then old.status else null end;
    v_new_status := new.status;
    v_amount := new.net_payment_total;

    if tg_op = 'INSERT' then
      v_activity_type := 'pay_period_created';
    elsif old.status is distinct from new.status then
      v_activity_type := case new.status
        when 'approved' then 'pay_period_approved'
        when 'paid' then 'pay_period_paid'
        when 'void' then 'pay_period_voided'
        else 'pay_period_updated'
      end;
    else
      v_activity_type := 'pay_period_updated';
    end if;

  elsif tg_table_name = 'payroll_batches' then
    v_studio_id := new.studio_id;
    v_pay_period_id := new.pay_period_id;
    v_payroll_batch_id := new.id;
    v_prior_status := case when tg_op = 'UPDATE' then old.status else null end;
    v_new_status := new.status;
    v_amount := new.net_payment_total;

    if tg_op = 'INSERT' then
      v_activity_type := 'batch_created';
    elsif old.status is distinct from new.status then
      v_activity_type := case new.status
        when 'approved' then 'batch_approved'
        when 'paid' then 'batch_paid'
        when 'void' then 'batch_voided'
        else 'batch_updated'
      end;
    else
      v_activity_type := 'batch_updated';
    end if;

  elsif tg_table_name = 'instructor_payroll_profiles' then
    v_studio_id := new.studio_id;
    v_instructor_id := new.instructor_id;
    v_activity_type := case
      when tg_op = 'INSERT' then 'payroll_profile_created'
      else 'payroll_profile_updated'
    end;
  end if;

  insert into public.compensation_activity_history (
    studio_id,
    instructor_id,
    earning_id,
    pay_period_id,
    payroll_batch_id,
    activity_type,
    prior_status,
    new_status,
    amount,
    actor_user_id,
    metadata
  )
  values (
    v_studio_id,
    v_instructor_id,
    v_earning_id,
    v_pay_period_id,
    v_payroll_batch_id,
    v_activity_type,
    v_prior_status,
    v_new_status,
    v_amount,
    auth.uid(),
    jsonb_build_object(
      'source_table', tg_table_name,
      'operation', tg_op
    )
  );

  return new;
end;
$$;

